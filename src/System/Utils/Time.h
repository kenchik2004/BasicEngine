#pragma once
namespace Time
{

#define MICRO2SEC(micro_sec) micro_sec*0.000001		//!< マイクロ秒 -> 秒への変換マクロ
#define SEC2MICRO(sec) sec*1000000					//!< 秒 -> マイクロ秒への変換マクロ
#define MILLI2SEC(micro_sec) micro_sec*0.001		//!< ミリ秒 -> 秒への変換マクロ 
#define SEC2MILLI(sec) sec*1000						//!< 秒 -> ミリ秒への変換マクロ


	//----------------------------------------------------
	// @brief 初期化する。
	//----------------------------------------------------
	int Init();
	//----------------------------------------------------
	// @brief 更新する。
	//----------------------------------------------------
	void Update();
	//----------------------------------------------------
	// @brief FPS を更新する。
	//----------------------------------------------------
	void UpdateFPS();


	//----------------------------------------------------
	// @brief 時飛ばし(時間の上書きなのであまり多用しないでください)。
	//----------------------------------------------------
	void ResetTime();


	//----------------------------------------------------
	// @brief 処理を指定FPSに固定する。
	//----------------------------------------------------
	int FixFPS();
	//----------------------------------------------------
	// @brief 描画処理の経過時間を初期化・描画FPSを計測する。
	//----------------------------------------------------
	void FixDrawFPS();
	//----------------------------------------------------
	// @brief 物理処理の経過時間を更新・物理処理のFPSを計測する。
	//----------------------------------------------------
	void FixFixedFPS();


	//----------------------------------------------------
	// @brief タイムスケールの取得(float)。
	//----------------------------------------------------
	const float TimeScale();
	//----------------------------------------------------
	// @brief タイムスケールの取得(double)。
	//----------------------------------------------------
	const double TimeScaleD();

	//----------------------------------------------------
	// @brief タイムスケールの変更。
	// @param scale タイムスケール。
	//----------------------------------------------------
	void SetTimeScale(const double scale);


	//----------------------------------------------------
	// @brief ゲーム内Δ時間の取得(float)。
	//----------------------------------------------------
	const float DeltaTime();
	//----------------------------------------------------
	// @brief ゲーム内Δ時間の取得(double)。
	//----------------------------------------------------
	const double DeltaTimeD();
	//----------------------------------------------------
	// @brief 描画用Δ時間の取得(float)。
	//----------------------------------------------------
	const float DrawDeltaTime();
	//----------------------------------------------------
	// @brief 描画用Δ時間の取得(double)。
	//----------------------------------------------------
	const double DrawDeltaTimeD();
	//----------------------------------------------------
	// @brief 物理用Δ時間の取得(float)。
	//----------------------------------------------------
	const float FixedDeltaTime();
	//----------------------------------------------------
	// @brief 物理用Δ時間の取得(double)。
	//----------------------------------------------------
	const double FixedDeltaTimeD();
	//----------------------------------------------------
	// @brief 前フレームとの物理的時間差の取得(float)。
	//----------------------------------------------------
	const float UnscaledDeltaTime();
	//----------------------------------------------------
	// @brief 前フレームとの物理的時間差の取得(double)。
	//----------------------------------------------------
	const double UnscaledDeltaTimeD();
	//----------------------------------------------------
	// @brief 前フレームとの実際の物理的時間差の取得(float)。
	//----------------------------------------------------
	const float RealDeltaTime();
	//----------------------------------------------------
	// @brief 前フレームとの実際の物理的時間差の取得(double)。
	//----------------------------------------------------
	const double RealDeltaTimeD();


	//----------------------------------------------------
	// @brief アプリケーション開始後の物理的時間の取得(float)。
	//----------------------------------------------------
	const float SystemTimeFromStart();
	//----------------------------------------------------
	// @brief アプリケーション開始後の物理的時間の取得(double)。
	//----------------------------------------------------
	const double SystemTimeFromStartD();
	//----------------------------------------------------
	// @brief アプリケーション開始後のゲーム内時間の取得(float)。
	//----------------------------------------------------
	const float GetTimeFromStart();
	//----------------------------------------------------
	// @brief アプリケーション開始後のゲーム内時間の取得(double)。
	//----------------------------------------------------
	const double GetTimeFromStartD();
	//----------------------------------------------------
	// @brief アプリケーション開始後の現実時間の取得(float)。
	//----------------------------------------------------
	const float GetRealTimeFromStart();
	//----------------------------------------------------
	// @brief アプリケーション開始後の現実時間の取得(double)。
	//----------------------------------------------------
	const double GetRealTimeFromStartD();

	//----------------------------------------------------
	// @brief システム内時間の取得(float)。
	//----------------------------------------------------
	const float SystemTime();
	//----------------------------------------------------
	// @brief システム内時間の取得(double)。
	//----------------------------------------------------
	const double SystemTimeD();
	//----------------------------------------------------
	// @brief Windowsから取得した現在時刻(float)。
	//----------------------------------------------------
	const float GetOSTime();
	//----------------------------------------------------
	// @brief Windowsから取得した現在時刻(double)。
	//----------------------------------------------------
	const double GetOSTimeD();


	//----------------------------------------------------
	// @brief FPSの最大値を取得(float)。
	//----------------------------------------------------
	const float GetFPSMAX();
	//----------------------------------------------------
	// @brief FPSの最大値を取得(double)。
	//----------------------------------------------------
	const double GetFPSMAXD();
	//----------------------------------------------------
	// @brief 描画FPSの最大値を取得(float)。
	//----------------------------------------------------
	const float GetDrawFPSMAX();
	//----------------------------------------------------
	// @brief 描画FPSの最大値を取得(double)。
	//----------------------------------------------------
	const double GetDrawFPSMAXD();

	//----------------------------------------------------
	// @brief FPSの最大値を設定。
	// @param max FPSの最大値。
	//----------------------------------------------------
	void SetFPSMAX(const double& max);
	//----------------------------------------------------
	// @brief 物理更新FPSの最大値を設定。
	// @param max 物理更新FPSの最大値。
	//----------------------------------------------------
	void SetFixedFPSMAX(const double& max);
	//----------------------------------------------------
	// @brief 描画FPSの最大値を設定。
	// @param max 描画FPSの最大値。
	//----------------------------------------------------
	void SetDrawFPSMAX(const double& max);


	//----------------------------------------------------
	// @brief 物理Δ時間の最大値取得(float)。
	//----------------------------------------------------
	const float GetFixedDeltaTimeMAX();
	//----------------------------------------------------
	// @brief 物理Δ時間の最大値取得(double)。
	//----------------------------------------------------
	const double GetFixedDeltaTimeMAXD();
	//----------------------------------------------------
	// @brief 描画Δ時間の最大値取得(float)。
	//----------------------------------------------------
	const float GetDrawDeltaTimeMAX();
	//----------------------------------------------------
	// @brief 描画Δ時間の最大値取得(double)。
	//----------------------------------------------------
	const double GetDrawDeltaTimeMAXD();
	//----------------------------------------------------
	// @brief Δ時間の最大値取得(float)。
	//----------------------------------------------------
	const float GetDeltaTimeMAX();
	//----------------------------------------------------
	// @brief Δ時間の最大値取得(double)。
	//----------------------------------------------------
	const double GetDeltaTimeMAXD();


	//----------------------------------------------------
	// @brief FPSの取得(float)。
	//----------------------------------------------------
	const float GetFPS();
	//----------------------------------------------------
	// @brief FPSの取得(double)。
	//----------------------------------------------------
	const double GetFPSD();
	//----------------------------------------------------
	// @brief 描画FPSの取得(float)。
	//----------------------------------------------------
	const float GetDrawFPS();
	//----------------------------------------------------
	// @brief 描画FPSの取得(double)。
	//----------------------------------------------------
	const double GetDrawFPSD();
};

